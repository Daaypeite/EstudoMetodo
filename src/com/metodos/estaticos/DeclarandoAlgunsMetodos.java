package com.metodos.estaticos;

public class DeclarandoAlgunsMetodos {

		//declarando um metodo com o modificador estatico
	public static void metodoEstatico() {
		//exibindo algum conteudo
		System.out.println("Este é o conteudo do metodo estatico");
	}
	//declarando um metodo sem o modificador estatico
	public void metodoNaoEstatico() {
		System.out.println("Conteudo metodo nao estatico");
	}
}
